void main(){
  //strings
  String firstName = "Layton";
  String lastName = "Musyoki";

  //numbers
  int age = 30;

  //double
  double height = 1.75;

  //dynamic
  dynamic age2 = 30;

  //boolean
  bool isEditing = true;
  bool isNotEditing = false;



  //final and const
  final String name = "Layton Musyoki";
  const String name2 = "Layton Musyoki";

  // String interpolation
  print("My name is $firstName $lastName");
  print("I am $age years old");
  print("I am $height meters tall");
  print("I am $age2 years old");
  print("My name is $name");
  print(isEditing);
  print(isNotEditing);
}